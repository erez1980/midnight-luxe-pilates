// Scans public/steps/ for generated illustrations and rewrites
// src/utils/stepImages.ts so any image you drop in is picked up automatically.
//
// Filename convention: <exerciseId>_<stepIndex>.png  (e.g. mat_hundred_0.png)
// Run: node scripts/build-step-images-map.mjs

import { readdirSync, writeFileSync, existsSync } from 'fs';

const DIR = 'public/steps';
const OUT = 'src/utils/stepImages.ts';

const files = existsSync(DIR)
  ? readdirSync(DIR).filter((f) => /\.(png|jpg|jpeg|webp|svg)$/i.test(f))
  : [];

const entries = [];
for (const file of files) {
  const match = file.match(/^(.+)_(\d+)\.[a-z]+$/i);
  if (!match) {
    console.warn(`skipping (unexpected name): ${file}`);
    continue;
  }
  entries.push(`  '${match[1]}:${match[2]}': './steps/${file}',`);
}
entries.sort();

writeFileSync(
  OUT,
  `// GENERATED by scripts/build-step-images-map.mjs — do not edit by hand.
// Add or remove files in public/steps/ and re-run the script.
//
// Keyed as \`<exerciseId>:<stepIndex>\` (0-based). Any entry here wins over the
// generated SVG pose illustration; anything absent falls back to SVG.
export const stepImages: Record<string, string> = {
${entries.join('\n')}
};

export function getStepImage(exerciseId: string, stepIndex: number): string | undefined {
  return stepImages[\`\${exerciseId}:\${stepIndex}\`];
}
`
);

console.log(`wrote ${entries.length} entries to ${OUT}`);
